package java.com;
public class Encapsulation {
	String name;
	int marks;
	 public void setName(String name)
	    {
	        this.name=name;
	    }
	    public void setMarks(int marks)
	    {
	        this.marks=marks;
	    }
	    public String getName()
	    {
	        return name;
	    } 
	    public int getMarks()
	    {
	        return marks;
	    }
	    
}
